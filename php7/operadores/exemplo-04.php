<?php

/* Operadores de Comparação */

$a = 30;

$b = 55;
// Maior que
var_dump($a > $b);
echo "</br>";
// Menor que
var_dump($a < $b);
echo "</br>";
// Igual a - Valor
var_dump($a == $b);
echo "</br>";
// Igual a - Tipo
var_dump($a === $b);
echo "</br>";
// Diferente / Não igual - Valor
var_dump($a != $b);
echo "</br>";
// Diferente - Tipo
var_dump($a !== $b);
echo "</br>";

?>